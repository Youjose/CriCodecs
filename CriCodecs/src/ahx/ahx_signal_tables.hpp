inline constexpr float AHX_SUBBAND_FILTER_FACTOR = 0.0490873852123405f;

consteval std::array<std::array<int64_t, 32>, 64> generate_subband_filter_matrix() {
    std::array<std::array<int64_t, 32>, 64> table{};
    for (size_t i = 0; i < table.size(); ++i) {
        for (size_t band = 0; band < table[i].size(); ++band) {
            const double angle =
                ((16.0 + static_cast<double>(i)) * ((static_cast<double>(band) * 2.0) + 1.0)) *
                static_cast<double>(AHX_SUBBAND_FILTER_FACTOR);
            table[i][band] = static_cast<int64_t>(util::cos(angle) * 268435456.0);
        }
    }
    return table;
}

