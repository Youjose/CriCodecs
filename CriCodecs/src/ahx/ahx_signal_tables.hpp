consteval std::array<int64_t, 512> generate_ahxsba_analysis_window_table() {
    std::array<int64_t, 512> table{};
    for (size_t i = 0; i < table.size(); ++i) {
        table[i] = static_cast<int64_t>(MPEG_LAYER2_ANALYSIS_WINDOW_Q21[i]) * (1ll << (AHX_FRAC_BITS - 21));
    }
    return table;
}

consteval std::array<int64_t, 512> generate_ahxsbf_synthesis_window_table() {
    std::array<int64_t, 512> table{};
    for (size_t i = 0; i < table.size(); ++i) {
        table[i] = static_cast<int64_t>(MPEG_LAYER2_SYNTHESIS_WINDOW_Q16[i]) * (1ll << (AHX_FRAC_BITS - 16));
    }
    return table;
}


consteval std::array<int64_t, 63> generate_scalefactor_table() {
    std::array<int64_t, 63> table{};
    for (size_t i = 0; i < table.size(); ++i) {
        table[i] = static_cast<int64_t>(util::exp2(1.0 - static_cast<double>(i) / 3.0) * 268435456.0 + 0.5);
    }
    return table;
}

consteval std::array<int64_t, 63> generate_inverse_scalefactor_table(
    const std::array<int64_t, 63>& scalefactors)
{
    std::array<int64_t, 63> table{};
    for (size_t i = 0; i < table.size(); ++i) {
        table[i] = static_cast<int64_t>((1ull << 56u) / static_cast<uint64_t>(scalefactors[i]));
    }
    return table;
}

