inline constexpr auto AHX_SF_TABLE = generate_scalefactor_table();
inline constexpr auto AHX_ISF_TABLE = generate_inverse_scalefactor_table(AHX_SF_TABLE);

inline constexpr auto AHX_SBA_ANALYSIS_WINDOW_TABLE = generate_ahxsba_analysis_window_table();
inline constexpr auto AHX_SBF_SYNTHESIS_WINDOW_TABLE = generate_ahxsbf_synthesis_window_table();

